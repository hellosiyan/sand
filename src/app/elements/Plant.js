export default class Plamt extends Drawable {
    constructor() {
        super();

        this.kind = '1';
        this.width = pixmaps[this.kind].width;
        this.height = pixmaps[this.kind].height;
    }

    draw(ctx) {
        const drawable = pixmaps[this.kind];
        drawable.x = this.x;
        drawable.y = this.y;

        ctx.globalAlpha = this.style.opacity;

        drawable.draw(ctx);
    }

    setKind(kind) {
        this.kind = kind;

        return this;
    }
}
